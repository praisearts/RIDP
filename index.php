		<p>Mankind is dependent on the soil for its needs for food and fiber for humans, feed for livestock, and, of late, contributing to our energy supply with crops grown primarily for biofuels. Soil is a dynamic and multifunctional living system that exists as a relatively thin layer on the Earth's crust. The various combinations of soil forming factors have given rise to an exceptional diversity of soil types across the world. The properties of soils and associated environmental conditions govern the various ecosystem functions of soil such as decomposition and transformation of organic wastes, mediating nutrient cycles, and influencing populations of soil organisms such as bacteria and fungi. While it is difficult to describe how well any soil performs its interrelated functions, earlier definitions included 'fertility', and later ones implied 'quality' and more recently 'soil health', a more inclusive term. </p>

		<p>Historically, where soils were fertile and capable of producing adequate crop yields, and where there was enough water, either as rainfall or irrigation, civilizations flourished. In the past century, world food production increased dramatically due to enhanced crop yields as a result of widespread adoption of technologies such as mechanization, new high-yielding and disease-resistant crop varieties, irrigation, and especially the use of mineral fertilizers. While crop yields were the primary focus in the past, awareness of increasing population growth and limited potential to bring more land into production led to the notion of cropping sustainability or sustainable intensification, i.e. consistently achieving high crop yields without damaging the soil's capacity to produce such yields.</p>

		<p> Thus, the current focus in soil and crop management is on maintenance of soil quality or soil health. That raises the issue of how fertilizer use affects the soil other than its effects on crop yields. While soil health can be affected by limited nutrient input from fertilizers, application of amounts of fertilizer nutrients above the crop's needs for optimum growth can be equally detrimental to soils and reduce economic profitability.</p>

